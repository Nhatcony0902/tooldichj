// ── Argument parsing ──────────────────────────────────────────────────────

/**
 * Parse CLI arguments from process.argv.
 * Returns a normalized options object.
 *
 * @param {string[]} argv
 * @returns {{ engine: string|null, outDir: string|null, types: string[], verbose: boolean,
 *             interactive: boolean, timeoutMs: number }}
 */
function parseArgs(argv) {
  const args = argv.slice(2);
  const opts = {
    engine: null,
    outDir: null,
    types: [...DEFAULT_CAPABILITY_TYPES],
    verbose: false,
    interactive: true,
    timeoutMs: DEFAULT_TIMEOUT_MS,
  };

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '--engine' && args[i + 1]) {
      opts.engine = args[++i];
      if (opts.engine === 'auto') opts.engine = null;
    } else if (arg === '--out-dir' && args[i + 1]) {
      opts.outDir = args[++i];
    } else if (arg === '--types' && args[i + 1]) {
      opts.types = args[++i].split(',').map(s => s.trim()).filter(Boolean);
    } else if (arg === '--verbose') {
      opts.verbose = true;
    } else if (arg === '--no-interactive') {
      opts.interactive = false;
    } else if (arg === '--timeout' && args[i + 1]) {
      const ms = parseInt(args[++i], 10);
      if (!Number.isNaN(ms) && ms > 0) opts.timeoutMs = ms;
    }
  }

  return opts;
}

