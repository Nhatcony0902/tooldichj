function writeFailureFile(sandboxRoot, type, adapterName, reason) {
  const fileName = `${type}.md`;
  const targetPath = path.join(sandboxRoot, fileName);
  const content =
    `<!-- Generated by t1k diagram refresh. -->\n` +
    `\u26a0\ufe0f Adapter ${adapterName} failed: ${reason}\n`;
  writeAtomic(targetPath, content);
  return fileName;
}

// ── README.md index builder ────────────────────────────────────────────────

/**
 * Build the content of README.md index.
 *
 * @param {object} opts
 * @param {string} opts.timestamp
 * @param {string|null} opts.gitSha
 * @param {string} opts.outDir
 * @param {Array<{ type: string, status: string, adapter: string|null, file: string|null, warnings: string[] }>} opts.rows
 * @returns {string}
 */
function buildReadme({ timestamp, gitSha, outDir, rows }) {
  const lines = [
    '# Diagram Index',
    '',
    `Last refreshed: ${timestamp}`,
    gitSha ? `Git SHA: \`${gitSha}\`` : 'Git SHA: (not in a git repo)',
    `Output directory: \`${outDir}\``,
    '',
    '## Capabilities',
    '',
    '| Type | Status | Adapter | File |',
    '|------|--------|---------|------|',
  ];

  for (const row of rows) {
    const statusIcon = row.status === 'ok' ? '\u2705' : '\u26a0\ufe0f';
    const adapterCell = row.adapter || '\u2014';
    const fileCell = row.file ? `[${row.file}](./${row.file})` : '\u2014';
    lines.push(`| ${row.type} | ${statusIcon} ${row.status} | ${adapterCell} | ${fileCell} |`);
  }

  lines.push('');

  // Warnings section
  const rowsWithWarnings = rows.filter(r => r.warnings && r.warnings.length > 0);
  if (rowsWithWarnings.length > 0) {
    lines.push('## Warnings', '');
    for (const row of rowsWithWarnings) {
      for (const w of row.warnings) {
        lines.push(`- **${row.type}**: ${w}`);
      }
    }
    lines.push('');
  }

  return lines.join('\n') + '\n';
}

// ── Meta file utilities ───────────────────────────────────────────────────

/**
 * Read existing .t1k-diagram-meta.json from outDir.
 * Returns an empty meta object if absent or unreadable.
 *
 * @param {string} outDir
 * @returns {object}
 */
function readMeta(outDir) {
  const metaPath = path.join(outDir, META_FILE_NAME);
  try {
    const raw = fs.readFileSync(metaPath, 'utf8');
    const parsed = JSON.parse(raw);
    if (parsed && typeof parsed.generated === 'object') return parsed;
  } catch {
    // absent or unreadable
  }
  return { generated: {} };
}

/**
 * Write .t1k-diagram-meta.json atomically.
 *
 * @param {string} outDir
 * @param {object} meta
 */
function writeMeta(outDir, meta) {
  const metaPath = path.join(outDir, META_FILE_NAME);
  writeAtomic(metaPath, JSON.stringify(meta, null, 2) + '\n');
}

// ── Version helper ─────────────────────────────────────────────────────────

/**
 * Resolve the t1k-preview skill version from its SKILL.md frontmatter.
 * Returns 'unknown' if unavailable.
 *
 * @returns {string}
 */
function getPreviewVersion() {
  try {
    const skillMd = path.resolve(__dirname, '..', 'SKILL.md');
    const content = fs.readFileSync(skillMd, 'utf8');
    const match = content.match(/^version:\s*(.+)$/m);
    return match ? match[1].trim() : 'unknown';
  } catch {
    return 'unknown';
  }
}

// ── Adapter version string ─────────────────────────────────────────────────

/**
 * Build a "name@version" string for an adapter.
 *
 * @param {object} adapter
 * @returns {string}
 */
function adapterVersionString(adapter) {
  // Try to read version from SKILL.md frontmatter
  try {
    const content = fs.readFileSync(adapter.skillMdPath, 'utf8');
    const match = content.match(/^version:\s*(.+)$/m);
    if (match) return `${adapter.skillName}@${match[1].trim()}`;
  } catch {
    // ignore
  }
  return adapter.skillName;
}

// ── Main orchestration logic ───────────────────────────────────────────────

/**
 * Run the full refresh pipeline.
 *
 * @param {object} opts - parsed CLI options
 * @returns {Promise<number>} exit code (0 or 1)
 */
async function runRefresh(opts) {
  const {
    engine: engineFilter,
    outDir: outDirFlag,
    types: requestedTypes,
    verbose,
    interactive,
    timeoutMs,
  } = opts;

  _verbose = verbose;

  const startTimestamp = new Date().toISOString();
  const cwd = process.cwd();

  // ── Step 1: Resolve output directory and sandbox root ──────────────────

  const outDir = path.resolve(cwd, outDirFlag || path.join('docs', 'diagrams'));
  logVerbose(`Output directory: ${outDir}`);

  try {
    fs.mkdirSync(outDir, { recursive: true });
  } catch (err) {
    logError(`Failed to create output directory '${outDir}': ${err.message}`);
    return EXIT_ERROR;
  }

  let sandboxRoot;
  try {
    sandboxRoot = fs.realpathSync(outDir);
  } catch (err) {
    logError(`Failed to resolve real path of '${outDir}': ${err.message}`);
    return EXIT_ERROR;
  }
  logVerbose(`Sandbox root (resolved): ${sandboxRoot}`);

  // ── Step 2: Acquire cross-command lock ─────────────────────────────────

  let lockHandle;
  try {
    lockHandle = await _acquireLock(DIAGRAM_LOCK_FILE, {
      command: 't1k diagram refresh',
      timeout: Number(process.env.T1K_DIAGRAM_LOCK_TIMEOUT_MS) || LOCK_TIMEOUT_MS,
    });
  } catch (err) {
    logError(err.message);
    return EXIT_ERROR;
  }

  try {
    return await _runRefreshWithLock({
      sandboxRoot,
      outDir,
      engineFilter,
      requestedTypes,
      interactive,
      timeoutMs,
      startTimestamp,
    });
  } finally {
    lockHandle.release();
  }
}

/**
 * Inner refresh logic — runs with lock held.
 *
 * @param {object} ctx
 * @returns {Promise<number>} exit code
 */
async function _runRefreshWithLock({
  sandboxRoot,
  outDir,
  engineFilter,
  requestedTypes,
  interactive,
  timeoutMs,
  startTimestamp,
}) {
  // ── Step 3: Discover adapters ──────────────────────────────────────────

  let discoveryResult;
  try {
    discoveryResult = discoverAdapters();
  } catch (err) {
    logError(`Adapter discovery failed: ${err.message}`);
    return EXIT_ERROR;
  }

  const { byEngine } = discoveryResult;
  const engineNames = Object.keys(byEngine);

  if (engineNames.length === 0) {
    process.stderr.write('[t1k] No adapter matched for the current project — nothing to refresh.\n');
    return EXIT_OK;
  }

  // ── Step 4: Engine selection + polyglot notice (H2) ────────────────────

  let selectedEngine;
  let selectedAdapter;

  if (engineFilter) {
    // --engine <name> override
    const candidates = byEngine[engineFilter];
    if (!candidates || candidates.length === 0) {
      logError(`No installed adapter found for engine '${engineFilter}'.`);
      return EXIT_ERROR;
    }
    // Resolve conflicts within engine
    selectedAdapter = _resolveEngineAdapter(engineFilter, candidates);
    selectedEngine = engineFilter;
  } else {
    // Auto: pick the highest-priority adapter across ALL engines
    let bestAdapter = null;
    let bestEngine = null;

    for (const [eng, candidates] of Object.entries(byEngine)) {
      const adapter = _resolveEngineAdapter(eng, candidates);
      if (!bestAdapter || adapter.priority > bestAdapter.priority) {
        bestAdapter = adapter;
        bestEngine = eng;
      }
    }

    selectedAdapter = bestAdapter;
    selectedEngine = bestEngine;

    // Polyglot notice for non-selected engines
    const otherEngines = engineNames.filter(e => e !== selectedEngine);
    if (otherEngines.length > 0) {
      process.stderr.write(`[t1k] Multiple adapters matched. Running: ${selectedEngine} (${adapterVersionString(selectedAdapter)})\n`);
      for (const other of otherEngines) {
        const otherAdapter = _resolveEngineAdapter(other, byEngine[other]);
        process.stderr.write(
          `[t1k] To run ${other}: t1k diagram refresh --engine ${other} --out-dir docs/diagrams-${other}\n`
        );
        logVerbose(`  skipping: ${adapterVersionString(otherAdapter)}`);
      }
    }
  }

  logVerbose(`Selected adapter: ${adapterVersionString(selectedAdapter)} (engine=${selectedEngine})`);

  // ── Read existing meta for hash protection ─────────────────────────────

  const existingMeta = readMeta(sandboxRoot);
  const metaGenerated = existingMeta.generated || {};

  // ── Step 5: For each requested capability type ─────────────────────────

  const rows = []; // for README
  const newMetaGenerated = {};
  let anyAdapterFailed = false;
  let allSkipped = true;

  for (const type of requestedTypes) {
    const adapterSupportsType = selectedAdapter.capabilities.includes(type);

    if (!adapterSupportsType) {
      // 5a: No adapter supports this type
      logVerbose(`Type '${type}' not supported by '${selectedEngine}' — writing not-available notice`);
      const relPath = writeNotAvailableFile(sandboxRoot, type);
      const targetPath = path.join(sandboxRoot, relPath);
      const fileHash = sha256File(targetPath);
      if (fileHash) {
        newMetaGenerated[relPath] = { sha256: fileHash, generatedAt: new Date().toISOString() };
      }
      rows.push({ type, status: 'not available', adapter: null, file: relPath, warnings: [] });
      continue;
    }

    allSkipped = false;

    // 5b: Pre-validate target path (fast fail before spawning)
    const expectedFileName = `${type}.md`;
    const expectedTarget = path.resolve(sandboxRoot, expectedFileName);
    try {
      assertWithinSandbox(sandboxRoot, expectedTarget);
    } catch (err) {
      logError(`Pre-validation failed for type '${type}': ${err.message}`);
      anyAdapterFailed = true;
      rows.push({ type, status: 'failed', adapter: adapterVersionString(selectedAdapter), file: null, warnings: [err.message] });
      continue;
    }

    // 5b: SHA-256 protection check
    const decision = await resolveOverwriteDecision(
      expectedTarget,
      expectedFileName,
      metaGenerated,
      interactive
    );

    if (decision === 'skip') {
      logVerbose(`Skipping '${type}' — user declined overwrite`);
      rows.push({ type, status: 'skipped (user)', adapter: adapterVersionString(selectedAdapter), file: expectedFileName, warnings: [] });
      // Preserve existing hash
      if (metaGenerated[expectedFileName]) {
        newMetaGenerated[expectedFileName] = metaGenerated[expectedFileName];
      }
      continue;
    }

    // 5d: Invoke generate.cjs
    let generateResult;
    try {
      generateResult = invokeGenerate(selectedAdapter, type, sandboxRoot, timeoutMs);
    } catch (err) {
      logWarn(`Type '${type}': ${err.message}`);
      const relPath = writeFailureFile(sandboxRoot, type, selectedAdapter.skillName, err.message);
      anyAdapterFailed = true;
      rows.push({ type, status: 'failed', adapter: adapterVersionString(selectedAdapter), file: relPath, warnings: [err.message] });
      continue;
    }

    // 5e: Sandbox check on returned path (symlink-safe)
    const returnedRelPath = generateResult.file;

    // Reject absolute paths or path traversal outright
    if (path.isAbsolute(returnedRelPath) || returnedRelPath.includes('..')) {
      const reason = `Adapter returned unsafe path: '${returnedRelPath}'`;
      logWarn(`Type '${type}': ${reason} — rejected`);
      anyAdapterFailed = true;
      rows.push({ type, status: 'failed (path traversal)', adapter: adapterVersionString(selectedAdapter), file: null, warnings: [reason] });
      continue;
    }

    const resolvedFile = path.resolve(sandboxRoot, returnedRelPath);

    try {
      assertWithinSandbox(sandboxRoot, resolvedFile);
    } catch (err) {
      logWarn(`Type '${type}': ${err.message} — rejected`);
      anyAdapterFailed = true;
      rows.push({ type, status: 'failed (sandbox)', adapter: adapterVersionString(selectedAdapter), file: null, warnings: [err.message] });
      continue;
    }

    try {
      assertNotSymlink(resolvedFile);
    } catch (err) {
      logWarn(`Type '${type}': ${err.message} — rejected`);
      anyAdapterFailed = true;
      rows.push({ type, status: 'failed (symlink)', adapter: adapterVersionString(selectedAdapter), file: null, warnings: [err.message] });
      continue;
    }

    // The adapter has already written the file to outDir per generate.cjs contract.
    // Handle sidecar case: if decision was 'sidecar', move the adapter-written file to .new
    if (decision === 'sidecar') {
      const sidecarPath = resolvedFile + '.new';
      try {
        if (fs.existsSync(resolvedFile)) {
          fs.renameSync(resolvedFile, sidecarPath);
        }
      } catch (err) {
        logWarn(`Could not create sidecar '${sidecarPath}': ${err.message}`);
      }
      // Keep existing hash in meta so next run still knows what T1K last wrote
      if (metaGenerated[returnedRelPath]) {
        newMetaGenerated[returnedRelPath] = metaGenerated[returnedRelPath];
      }
      rows.push({
        type,
        status: 'sidecar written',
        adapter: adapterVersionString(selectedAdapter),
        file: returnedRelPath + '.new',
        warnings: [`User-edited '${returnedRelPath}' preserved; new version written to '${returnedRelPath}.new'`],
      });
      continue;
    }

    // 5f: Update meta with new hash
    const fileHash = sha256File(resolvedFile);
    if (fileHash) {
      newMetaGenerated[returnedRelPath] = {
        sha256: fileHash,
        generatedAt: new Date().toISOString(),
      };
    }

    for (const w of generateResult.warnings) {
      logWarn(`[${type}] ${w}`);
    }

    rows.push({
      type,
      status: 'ok',
      adapter: adapterVersionString(selectedAdapter),
      file: returnedRelPath,
      warnings: generateResult.warnings,
    });

    log(`  ${type.padEnd(12)} \u2705 (${adapterVersionString(selectedAdapter)})`);
  }

  // ── Step 6: Write README.md index ──────────────────────────────────────

  const gitSha = getGitSha();

  // SHA-256 protect README.md too
  const readmePath = path.join(sandboxRoot, README_FILE_NAME);
  const readmeDecision = await resolveOverwriteDecision(
    readmePath,
    README_FILE_NAME,
    metaGenerated,
    interactive
  );

  if (readmeDecision !== 'skip') {
    const readmeContent = buildReadme({
      timestamp: startTimestamp,
      gitSha,
      outDir: sandboxRoot,
      rows,
    });

    if (readmeDecision === 'sidecar') {
      const sidecarPath = readmePath + '.new';
      writeAtomic(sidecarPath, readmeContent);
      logWarn(`README.md has been modified — written to README.md.new`);
      if (metaGenerated[README_FILE_NAME]) {
        newMetaGenerated[README_FILE_NAME] = metaGenerated[README_FILE_NAME];
      }
    } else {
      writeAtomic(readmePath, readmeContent);
      const readmeHash = sha256File(readmePath);
      if (readmeHash) {
        newMetaGenerated[README_FILE_NAME] = {
          sha256: readmeHash,
          generatedAt: new Date().toISOString(),
        };
      }
    }
  } else {
    if (metaGenerated[README_FILE_NAME]) {
      newMetaGenerated[README_FILE_NAME] = metaGenerated[README_FILE_NAME];
    }
  }

  // ── Step 7: Write .t1k-diagram-meta.json (atomic) ─────────────────────

  const meta = {
    timestamp: startTimestamp,
    git_sha: gitSha,
    adapters: [adapterVersionString(selectedAdapter)],
    out_dir: sandboxRoot,
    t1k_preview_version: getPreviewVersion(),
    generated: newMetaGenerated,
  };

  writeMeta(sandboxRoot, meta);
  logVerbose(`Meta file written: ${path.join(sandboxRoot, META_FILE_NAME)}`);

  // ── Summary output ─────────────────────────────────────────────────────

  log(`Refresh complete \u2014 ${outDir}`);
  for (const row of rows) {
    const icon = row.status === 'ok'
      ? '\u2705'
      : row.status.startsWith('not available') || row.status.startsWith('skipped')
        ? '\u26a0\ufe0f '
        : '\u274c';
    const fileInfo = row.file ? ` ${row.file}` : '';
    log(`  ${(row.type + fileInfo).padEnd(24)} ${icon} ${row.status}` + (row.adapter ? ` (${row.adapter})` : ''));
  }

  if (allSkipped) {
    process.stderr.write('[t1k] No capabilities were generated — all types skipped or not available.\n');
  }

  // Exit non-zero only if ALL adapters failed (at least one succeeded or was not-available is ok)
  const failedRows = rows.filter(r => r.status === 'failed' || r.status.startsWith('failed'));
  if (anyAdapterFailed && failedRows.length === rows.length) {
    logError('All capabilities failed.');
    return EXIT_ERROR;
  }

  return EXIT_OK;
}

// ── Conflict resolution within an engine (re-export for tests) ─────────────

/**
 * Given engine candidates array, resolve to the single best adapter.
 * Throws on genuine tie.
 *
 * @param {string} engine
 * @param {object[]} candidates
 * @returns {object}
 */
function _resolveEngineAdapter(engine, candidates) {
  if (candidates.length === 1) return candidates[0];

  const sorted = [...candidates].sort((a, b) => {
    if (b.priority !== a.priority) return b.priority - a.priority;
    return a.skillName.localeCompare(b.skillName);
  });

  const winner = sorted[0];
  const runnerUp = sorted[1];

  if (winner.priority === runnerUp.priority && winner.skillName === runnerUp.skillName) {
    throw new Error(
      `Hard tie for engine '${engine}': adapters '${winner.skillName}' and '${runnerUp.skillName}' ` +
      `have identical priority. Increase one adapter's priority.`
    );
  }

  return winner;
}

