/**
 * refresh-orchestrator.cjs — Drives `t1k diagram refresh` and `/t1k:preview --refresh`.
 *
 * Responsibilities (per references/refresh-orchestrator.md):
 *   1. Resolve output directory + sandbox root (symlink-safe via realpathSync).
 *   2. Acquire cross-command lock at ~/.claude/.diagram.lock (stale-lock recovery).
 *   3. Discover adapters via adapter-discovery.cjs.
 *   4. Polyglot notice (H2): multiple engines → run highest-priority + emit re-invocation hints.
 *   5. For each requested capability type → invoke generate.cjs → sandbox-check output.
 *   6. SHA-256 overwrite protection (BLOCKER 5): prompt or .new sidecar on hash mismatch.
 *   7. Write README.md index + .t1k-diagram-meta.json (atomic temp+rename).
 *   8. Release lock in try/finally.
 *
 * CLI flags:
 *   --engine <name|auto>    single-engine override; default auto
 *   --out-dir <path>        default: docs/diagrams/
 *   --types <csv>           default: modules,classes,packages,c4
 *   --verbose               log every step
 *   --no-interactive        force non-interactive (sidecar on hash mismatch)
 *   --timeout <ms>          per-adapter timeout; default 120000
 *
 * Cross-platform: uses fs.readFileSync(0), os.tmpdir(), path.join(), process.platform,
 * windowsHide: true. Uses execFileSync (never execSync with shell). All subprocess
 * invocations in a symlink-safe cwd.
 */

